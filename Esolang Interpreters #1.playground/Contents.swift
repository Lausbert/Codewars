func interpreter(_ prog: String) -> String {
    let prog = String(prog.characters.filter {[".", "+"].contains($0)})
    var result = "", cell:UInt8 = 0
    for comm in prog.characters {
        switch comm {
        case "+":
            cell = cell == 255 ? 0 : cell + 1
        case ".":
            result += String(UnicodeScalar(cell))
        default:
            break
        }
    }
    return result
}

